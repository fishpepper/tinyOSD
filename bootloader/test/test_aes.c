// prints string as hex
static void phex(uint8_t* str, uint8_t len){
    unsigned char i;
    for(i = 0; i < len; ++i) {
        printf("%.2x", str[i]);
        if (i%16 == 15) printf("\n");
    }
    printf("\n");
}


